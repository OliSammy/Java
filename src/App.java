public class App {
    int add (int a, int b) {
        return a + b;
    }
    int subtract (int a, int b) {
        return a - b;
    }
    int multiply (int a, int b) {
        return a * b;
    }
    int divide (int a, int b) {
        return a / b;
    }
    int modulo (int a, int b) {
        return a % b;
    }   
    int power (int a, int b) {
        return (int) Math.pow(a, b);
    }
 }
